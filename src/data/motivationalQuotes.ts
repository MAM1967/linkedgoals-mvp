/**
 * Get the next motivational quote in sequence
 * Uses localStorage to track progress and cycles through all quotes
 */
export const getNextMotivationalQuote = (): MotivationalQuote => {
  const currentIndex = getCurrentQuoteIndex();
  const quote = motivationalQuotes[currentIndex];

  // Increment for next time, cycling back to 0 when we reach the end
  const nextIndex = (currentIndex + 1) % motivationalQuotes.length;
  setCurrentQuoteIndex(nextIndex);

  return quote;
};

/**
 * Get current quote index from localStorage
 */
export const getCurrentQuoteIndex = (): number => {
  try {
    const stored = localStorage.getItem("linkedgoals_quote_index");
    if (stored === null) return 0;

    const index = parseInt(stored, 10);
    // Ensure index is valid, reset to 0 if corrupted
    return isNaN(index) || index < 0 || index >= motivationalQuotes.length
      ? 0
      : index;
  } catch (error) {
    // localStorage might not be available in some environments
    console.warn("Could not access localStorage for quote index:", error);
    return 0;
  }
};

/**
 * Set current quote index in localStorage
 */
export const setCurrentQuoteIndex = (index: number): void => {
  try {
    localStorage.setItem("linkedgoals_quote_index", index.toString());
  } catch (error) {
    // localStorage might not be available in some environments
    console.warn("Could not save quote index to localStorage:", error);
  }
};

/**
 * Reset quote progression (useful for testing or admin purposes)
 */
export const resetQuoteProgression = (): void => {
  setCurrentQuoteIndex(0);
};

/**
 * Get a random quote (useful for preview or fallback)
 */
export const getRandomMotivationalQuote = (): MotivationalQuote => {
  const randomIndex = Math.floor(Math.random() * motivationalQuotes.length);
  return motivationalQuotes[randomIndex];
};
